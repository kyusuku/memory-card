interface CardsProps {
  onCardClick: (pokemon: string) => void;
}

const Cards = ({ onCardClick }: CardsProps) => {
  let count = 0;
  const pokemonDisplay = new Set<string>();
  while (count < 12) {
    const randomIndex = Math.floor(Math.random() * pokemons.length);
    if (!pokemonDisplay.has(pokemons[randomIndex])) {
      pokemonDisplay.add(pokemons[randomIndex]);
      count++;
    }
  }

  const handleClick = (pokemon: string) => {
    onCardClick(pokemon);
  };

  return (
    <>
      {Array.from(pokemonDisplay).map((pokemon: string) => (
        <div
          key={pokemon}
          className="flex min-h-[300px] flex-col rounded-[15px] bg-[#fff] p-2 opacity-[0.85] shadow-2xl hover:bg-blue-200 hover:shadow-blue-300 max-2xl:min-h-0 max-lg:min-h-0"
          onClick={() => handleClick(pokemon)}
        >
          <Fetch name={pokemon} />
          <p className="text-center text-ellipsis max-xl:text-[12px] max-lg:text-[12px] max-sm:text-[8px]">
            {pokemon}
          </p>
        </div>
      ))}
    </>
  );
};

export default Cards;
